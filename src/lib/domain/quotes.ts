/** يُرجع عبارة عشوائية مختلفة عن آخر مرة قدر الإمكان */
export function randomQuote(lastId?: number): { quote: Quote; index: number } {
  let idx: number;
  if (QUOTES.length <= 1) {
    idx = 0;
  } else {
    do {
      idx = Math.floor(Math.random() * QUOTES.length);
    } while (idx === lastId);
  }
  return { quote: QUOTES[idx], index: idx };
}

